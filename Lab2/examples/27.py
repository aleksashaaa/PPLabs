print(x)

x = thisdict.get("model")
print(x)

x = thisdict.keys()
print(x) # dict_keys(['brand', 'model', 'year'])

car = {
"brand": "Ford",
"model": "Mustang",
"year": 1964
}
x = car.keys()
print(x) #before the change
car["color"] = "white"
print(x) #after the change

x = thisdict.values() 
